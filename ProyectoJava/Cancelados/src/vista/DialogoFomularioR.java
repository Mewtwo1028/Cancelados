    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String contra1 = jTextField1.getText();
        String contra2 = jTextField2.getText();
        String pass = "";

        if (contra1.isEmpty() || contra2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "¡Error! Campos de entrada vacios!");
            return;
        }

        if (!contra1.equals(contra2)) {
            JOptionPane.showMessageDialog(this, "¡Error! Campos de entrada no coinciden!");
            return;
        }

        try {
            pass = encriptaContra(contra1);
        } catch (NoSuchAlgorithmException ex) {
            System.out.println("¡Error! No se pudo encriptar la contraseña");
            return;
        }

        if (new Empleado().modificaCredenciales(new Credencial(pass, idEmpleado))) {
            if (new EmpleadoManager().eliminarEstado(idEmpleado)) {
                JOptionPane.showMessageDialog(this, "EXITO");
            }
        } else {
            JOptionPane.showMessageDialog(this, "¡Error NO SE PUDO!");
        }
        
        this.dispose();

    }//GEN-LAST:event_jButton1MouseClicked

    private String encriptaContra(String contrasena) throws NoSuchAlgorithmException {
        MessageDigest md = MessageDigest.getInstance("MD5");
        byte[] messageDigest = md.digest(contrasena.getBytes());

        BigInteger bigInt = new BigInteger(1, messageDigest);

        return bigInt.toString();
    }

