    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        //DialogoEmergente dEmergente = new DialogoEmergente(this, true);
        int idVenta = 0;
        
        try {
            idVenta = Integer.parseInt(tblVenta.getValueAt(tblVenta.getSelectedRow(), 0).toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR! Debe de Seleccionar una venta de la tabla");
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Estas seguro?") == JOptionPane.OK_OPTION) {

            if (new VentaManager().cancelarVenta(new Venta(idVenta, "DEVUELTO"))) {
                JOptionPane.showMessageDialog(this, "Estado de la venta modificaro de forma exitosa!");
            }

        }

        llenarTabla();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnVerDetalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerDetalleActionPerformed

        //Obtener id de la venta
        try {
            int idVenta = Integer.parseInt(tblVenta.getValueAt(tblVenta.getSelectedRow(), 0).toString());
            float total = Float.parseFloat(tblVenta.getValueAt(tblVenta.getSelectedRow(), 1).toString());
            String estado = tblVenta.getValueAt(tblVenta.getSelectedRow(), 5).toString();
            //Crear dialogo y enviar datos
            DialogoDetalleVenta dDetalle = new DialogoDetalleVenta(this, true, idVenta, total, estado);
            dDetalle.setLocationRelativeTo(null);
            //mostrar jdialog
            dDetalle.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR! Debe de Seleccionar una venta de la tabla");
        }


    }//GEN-LAST:event_btnVerDetalleActionPerformed

