    private boolean existeCategoria(String nombreCategoria) {

        Object[] c = new CategoriaManager().getCategorias();

        for (Object c1 : c) {
            if (c1.toString().toLowerCase().equals(nombreCategoria.toLowerCase())) {
                return true;
            }
        }

        return false;
    }

    private boolean registrarCategoria(String nombreCategoria) {
        return new CategoriaManager().registrarCategoria(nombreCategoria);
    }

