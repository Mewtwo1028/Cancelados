    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        String nuevaCategoria = jTextField1.getText().trim();

        if (existeCategoria(nuevaCategoria)) {
            JOptionPane.showMessageDialog(this, "La categoria que se esta intentando registrar ya existe", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (registrarCategoria(nuevaCategoria)) {
            JOptionPane.showMessageDialog(this, "Se registro de forma correcta");
            this.dispose();
            return;
        }

        JOptionPane.showMessageDialog(this, "ERROR AL REGISTRAR LA CATEGORIA");

    }//GEN-LAST:event_jButton1MouseClicked

